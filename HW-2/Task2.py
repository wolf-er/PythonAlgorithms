'''2. Посчитать четные и нечетные цифры введенного натурального числа. Например,
если введено число 34560, то у него 3 четные цифры (4, 6 и 0) и 2 нечетные (3 и 5).'''

num = int(input("Ведите натуральное число "))
numEven = 0
numOdd = 0
while num > 0:
    res = num % 10
    if res % 2:
        numOdd += 1
    else:
        numEven += 1
    num //= 10
print('Во введенном числе {} четных цифр и {} нечетных'.format(numEven, numOdd))